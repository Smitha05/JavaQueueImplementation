package com.cc.task1;
public class PrimeNumber {

	int rnum;
	boolean isPrime;

	public PrimeNumber() {
	}

	public PrimeNumber(int rnum, boolean isPrime) {
		this.rnum = rnum;
		this.isPrime = isPrime;
	}

	public int getRnum() {
		return rnum;
	}

	public boolean isPrime() {
		return isPrime;
	}

}
